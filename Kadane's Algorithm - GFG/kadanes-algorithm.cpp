class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        
        int currsum =0;
        int max_sum = INT_MIN;
        int i=0;
        while( i<n){
            currsum += arr[i];
            
            max_sum = max( max_sum, currsum);
            
            if(currsum < 0){
                currsum = 0;
            }
            
            
            
            i++;
        }
        return max_sum;
    }
};

